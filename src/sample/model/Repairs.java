package sample.model;

public class Repairs {
    public int repairID;
    public int repairLocID;
    public int repairMonth;
    public int repairYear;
    public int repairCount;

    public void setRepairID(int repairID) {
        this.repairID = repairID;
    }

    public int getRepairID() {
        return repairID;
    }

    public void setRepairLocID(int repairLocID) {
        this.repairLocID = repairLocID;
    }

    public int getRepairLocID() {
        return repairLocID;
    }

    public void setRepairMonth(int repairMonth) {
        this.repairMonth = repairMonth;
    }

    public int getRepairMonth() {
        return repairMonth;
    }

    public void setRepairYear(int repairYear) {
        this.repairYear = repairYear;
    }

    public int getRepairYear() {
        return repairYear;
    }

    public void setRepairCount(int repairCount) {
        this.repairCount = repairCount;
    }

    public int getRepairCount() {
        return repairCount;
    }
}
